// segments have a required length and are not inflated at first
DictionarySegment::DictionarySegment( int requiredLength ) : mInflated( false ), mValue( ' ' ), mRequiredLength( requiredLength )
{
    
}

void DictionarySegment::setRequiredLength( int i )
{
    mRequiredLength = i;
}

void DictionarySegment::forWhichValues( char c )
{
    mValue = c;
}

bool DictionarySegment::hasValues( char c )
{
    return( mValue == c );
}

// search the segment for a particular word
// all words in a segment start with the mValue letter so filter first on that
bool DictionarySegment::lookup( std::string s )
{
    bool result = false;
    // to search, this segment must be inflated first
    if (!mInflated)
    {
        inflate();
        mInflated = true;
    }
    if (s.length() == mRequiredLength && s[0] == mValue)
    {
        auto locate = mSegment.find( s );
        if (locate != mSegment.end())
        {
            result = true;
        }
    }
    return( result );
}

// select the word found at a particular position
// this is used to randomly pick a word
std::string DictionarySegment::valueAt( int spot )
{
    std::string result;
    // to search, this segment must be inflated first
    if (!mInflated)
    {
        inflate();
        mInflated = true;
    }
    int i = 0;
    bool keepGoing = true;
    while( keepGoing )
    {
        for(std::set< std::string >::const_iterator iter = mSegment.cbegin();
            iter != mSegment.cend();
            iter++)
        {
            if (i == spot)
            {
                result = *iter;
                keepGoing = false;
                break;
            }
            i++;
        }
    }
    return( result ); // just for now...
}

// toss out the in-memory database
void DictionarySegment::deflate()
{
    if (mInflated)
    {
        mSegment.clear();
    }
}

